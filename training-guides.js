/**
 * 获取指定目标的引导文案列表
 * @param {string} targetId - 训练目标 ID
 * @returns {string[]} 引导文案数组
 */
function getTrainingGuides(targetId) {
  return TRAINING_GUIDES[targetId] || TRAINING_GUIDES.focus;
}

/**
 * 获取随机引导文案（不重复直到全部显示完）
 * @param {string} targetId - 训练目标 ID
 * @param {number} index - 当前索引
 * @returns {string} 引导文案
 */
function getGuideByIndex(targetId, index) {
  const guides = getTrainingGuides(targetId);
  return guides[index % guides.length];
}
